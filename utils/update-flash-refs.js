var fs = require('fs');
var path = require('path');

function listJSFiles(dir, prefix, output) {
  var files = fs.readdirSync(dir);
  for (var i = 0; i < files.length; i++) {
    var file = files[i];
    var s = fs.statSync(path.join(dir, file));
    if (s.isDirectory()) {
      listJSFiles(path.join(dir, file), prefix + file + '/', output);
    } else if (s.isFile() && /\.js$/i.test(file) && prefix != '') {
      output.push(prefix + file);
    }
  }
}

function updateFlashRefs(filePath, flashDir) {
  var content = fs.readFileSync(filePath, 'utf8');
  var m = /\n([\t\u0020]+)<!-- Autogenerated flash references: base=(\S+)/.exec(content);
  if (!m) {
    throw new Error(filePath + ' has no flash references');
  }

  var baseUrl = m[2], indent = m[1];
  var lineStart = indent + '<script src="' + baseUrl;
  var lineEnd = '"></script>';

  var list = [];
  listJSFiles(flashDir, '', list);
  list.sort(function (a, b) {
    return a.toLowerCase() < b.toLowerCase() ? -1 : 1;
  });

  content = content.replace(/(Autogenerated flash references:.*?\n)[\S\s]*?(\n\s+<!-- Autogenerated flash references end)/,
    function (all, head, tail) {
      return head + lineStart + list.join(lineEnd + '\n' + lineStart) + lineEnd + tail;
    }
  );

  fs.writeFileSync(filePath, content, 'utf8');
}

module.exports = updateFlashRefs;
