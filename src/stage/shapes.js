var Shumway;
(function (Shumway) {
    
    var Rectangle = Shumway.Geometry.Rectangle;

    var SHAPE_ROOT = "assets/shapes/";

    function loadShape(file) {
        var path = SHAPE_ROOT + file;
        var gl = this.gl;
        var request = new XMLHttpRequest();
        request.open("GET", path, false);
        request.send();

        // assert (request.status === 200, "File : " + path + " not found.");
        return JSON.parse(request.responseText);
    }

    function parseRGBColor(value, alpha) {
        return "rgba(" + (((value >> 16) & 0xFF)) + ", " + (((value >> 8) & 0xFF)) + ", " + (((value) & 0xFF)) + ", " + alpha + ")";
    }

    var ShapeSource = (function () {
        function ShapeSource(data) {
            this.properties = {};
            this._data = data;
        }
        ShapeSource.prototype.getBounds = function () {
            var b = this._data.shapeBounds;
            return new Rectangle(b.xmin, b.ymin, b.xmax - b.xmin, b.ymax - b.ymin);
        };
        ShapeSource.prototype.render = function (context) {
            var bounds = this.getBounds();
            context.save();
            context.translate(-bounds.x, -bounds.y);
            var shapes = this._data.shapes;
            for (var i = 0; i < shapes.length; i++) {
                var shape = shapes[i];
                var paths = shape.geometry.paths;
                for (var j = 0; j < paths.length; j++) {
                    var path = paths[j];
                    context.save();
                    context.beginPath();
                    context.moveTo(path[0][0], path[0][1]);
                    for (var k = 0; k < path.length; k++) {
                        var command = path[k];
                        if (command.length === 2) {
                            context.lineTo(command[0], command[1]);
                        } else if (command.length === 4) {
                            context.quadraticCurveTo(command[0], command[1], command[2], command[3]);
                        }
                    }
                    context.closePath();
                    if (shape.fill) {
                        if (shape.fill.type === 0) {
                            context.fillStyle = parseRGBColor(shape.fill.color, shape.fill.alpha);
                        }
                        context.fill();
                    }
                    context.restore();
                }
            }
            context.restore();
        };
        return ShapeSource;
    })();
    Shumway.ShapeSource = ShapeSource;

    Shumway.Shapes = [
        new ShapeSource(loadShape("sword.json")),
        new ShapeSource(loadShape("decorate.json")),
        new ShapeSource(loadShape("background.json")),
        new ShapeSource(loadShape("fire.json"))
    ];

    loadShape("assets.json").forEach(function (shape) {
        Shumway.Shapes.push(new ShapeSource(shape));
    });

    function getRandomShape() {
        return Shumway.Shapes[Math.random() * Shumway.Shapes.length | 0];
    }
    Shumway.getRandomShape = getRandomShape;
})(Shumway || (Shumway = {}));

